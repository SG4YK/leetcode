/**
 * Definition for singly-linked list. class ListNode { int val; ListNode next;
 * ListNode(int x) { val = x; next = null; } }
 */
public class Solution {
    public ListNode detectCycle(ListNode head) {
        if (head == null || head.next == null) {
            return null;
        }

        ListNode fast = head, slow = head;
        int steps = 0; // this represents the number of nodes in a cycle
        do {
            if (fast == null || fast.next == null) {
                return null;
            }
            steps++;
            slow = slow.next;
            fast = fast.next.next;
        } while (fast != slow);

        slow = head;
        fast = head;

        // make fast pointer a cycle ahead of slow pointer
        for (int i = 0; i < steps; i++) {
            fast = fast.next;
        }

        // when slow reaches fast, slow will be at the start of the cycle
        while (fast != slow) {
            fast = fast.next;
            slow = slow.next;
        }
        return slow;
    }
}
